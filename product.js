// Get all filter buttons
const filterButtons = document.querySelectorAll('.sortfeature button');

// Add event listeners to each filter button
filterButtons.forEach(button => {
    button.addEventListener('click', function() {
        // Get the filter criteria from the button's text content
        const filterCriteria = this.textContent.trim();
        // Call the filterProducts function with the selected criteria
        filterProducts(filterCriteria);
    });
});

// Define the filterProducts function
function filterProducts(criteria) {

  if (criteria === "All") {
    // Render all products
    renderProducts(slidersProducts.products);
    return;
}
    // Parse the criteria to get the price range
    const [minPrice, maxPrice] = criteria.split(' ~ ').map(str => parseInt(str.replace(/[^\d]/g, '')));

    // Filter products based on price range
    const filteredProducts = slidersProducts.products.filter(product => {
        const price = parseInt(product.newPrice.replace(/[^\d]/g, ''));
        return price >= minPrice && price <= maxPrice;
    });

    // Render the filtered products
    renderProducts(filteredProducts);
}

// Define the renderProducts function to display the filtered products
function renderProducts(products) {
    let outPut = "";
    products.forEach((product, index) => {
        outPut += `
            <div class="slide" id="product_${index}">
                <img src="${product.imageSrc}" alt="Product image" />
                <div class="product-info">
                    <h2 class="product-title">${product.name}</h2>
                    <p class="product-details">
                        <span class="product-price-old">${product.oldPrice}</span>
                        <span class="product-price-new">${product.newPrice}</span>
                    </p>
                    <div class="product-rating">
                        ${'<span class="star">★</span>'.repeat(product.rating)}
                    </div>
                    <div class="view">
                        <button onclick="displayBlock('overlay', 'products', ${index})">View</button>
                    </div>
                </div>
            </div>
        `;
    });
    document.getElementById("productall").innerHTML = outPut;
}

